    private void preencherTabela() {
        ClientesDAO clienteDao = new ClientesDAO();
        String nomeCliente = txtPesquisaNome.getText();
        List<Clientes> listaClientes = clienteDao.getCliente(nomeCliente);

        DefaultTableModel tabelaClientes = (DefaultTableModel) tblListaClientes.getModel();
        tabelaClientes.setNumRows(0);

        tblListaClientes.setRowSorter(new TableRowSorter(tabelaClientes));

        for (Clientes c : listaClientes) {
            Object[] obj = new Object[]{
                c.getId(),
                c.getNome(),
                c.getCpf_cnpj(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUF(),
                c.getCep(),
                c.getEmail(),
                c.getTelefone()
            };
            tabelaClientes.addRow(obj);
        }

    }

