    private void btnFotovoltaicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFotovoltaicoActionPerformed
        TelaCadastroFotovoltaico cadastrarFV = new TelaCadastroFotovoltaico();        
        int posUser = tblListaClientes.getSelectedRow();
        if (posUser >= 0) {
            int idCliente = (int) tblListaClientes.getValueAt(posUser, 0);
            cadastrarFV.PegaIDclienteFK(idCliente);
            dispose();
            cadastrarFV.setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente");
        }
    }//GEN-LAST:event_btnFotovoltaicoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        TelaCadastroCliente telaCadastro = new TelaCadastroCliente();
        
        int posUser = tblListaClientes.getSelectedRow();
        if (posUser >= 0) { // Verifica se há linha selecionada na tabela
            int idCliente = (int) tblListaClientes.getValueAt(posUser, 0);
            telaCadastro.PegaIDcliente(idCliente); // Passa o usuário selecionado para a tela de cadastro
            dispose();
            telaCadastro.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

