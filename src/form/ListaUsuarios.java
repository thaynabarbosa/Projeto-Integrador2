 public void validaPermissao(String permissao) {
        this.permissao = permissao;
        if (permissao.equals("Projetista")) {
            btnEditar.setEnabled(false);
            bntVoltar.setEnabled(false);
        } else if (permissao.equals("Vendedor")) {
            btnEditar.setEnabled(false);
            bntVoltar.setEnabled(false);
        }
    }
