    private void preecherTabela() {
        UsuarioDAO usuarioDao = new UsuarioDAO();
        String nomeUsuario = txtPesquisaNome.getText();
        List<Usuarios> listaUsuarios = usuarioDao.getUsuario(nomeUsuario);

        DefaultTableModel tabelaUsuarios = (DefaultTableModel) tblUsuarios.getModel();
        tabelaUsuarios.setNumRows(0);
        tblUsuarios.setRowSorter(new TableRowSorter(tabelaUsuarios));

        for (Usuarios user : listaUsuarios) {
            Object[] obj = new Object[]{
                user.getId(),
                user.getNome(),
                user.getLogin(),
                user.getSenha(),
                user.getPermissao()
            };
            tabelaUsuarios.addRow(obj);
        }

    }

