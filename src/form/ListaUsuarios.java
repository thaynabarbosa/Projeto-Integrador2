    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        TelaCadastrarUsuario telaCadastro = new TelaCadastrarUsuario();
        telaCadastro.validaPermissao(permissao);
        int posUser = tblUsuarios.getSelectedRow();
        if (posUser >= 0) { // Verifica se há linha selecionada na tabela
            int idCliente = (int) tblUsuarios.getValueAt(posUser, 0);
            telaCadastro.PegaIDUsuario(idCliente); // Passa o usuário selecionado para a tela de cadastro
            dispose();
            telaCadastro.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

